angka_pertama = 24
angka_kedua = 5

tambah = angka_pertama + angka_kedua
kurang = angka_pertama - angka_kedua
kali = angka_pertama * angka_kedua
bagi = angka_pertama / angka_kedua
pangkat = angka_kedua ** 2
modulus = angka_pertama % angka_kedua

print(f"Hasil tambah {angka_pertama} + {angka_kedua} = {tambah}")
print(f"Hasil kurang {angka_pertama} - {angka_kedua} = {kurang}")
print(f"Hasil kali {angka_pertama} * {angka_kedua} = {kali}")
print(f"Hasil bagi {angka_pertama} / {angka_kedua} = {bagi}")
print(f"Hasil pangkat {angka_kedua} pangkat 2 = {pangkat}")
print(f"Hasil modulus {angka_pertama} % {angka_kedua} = {modulus}")